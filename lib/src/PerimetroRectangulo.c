/*
@brief Calcula el area de un rectangulo
@param a valor de ancho del rectangulo, b valor del largo del rectangulo
@return Resultado del perimetro del rectangulo 

*/

 float PerimetroRectangulo (float a, float b) 
 {
	float  r; 
	r=(2*a)+(2*b);
 
  return r;
}